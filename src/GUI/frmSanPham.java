    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        String tuKhoa = txt_tuKhoa.getText();
        if (tuKhoa.equals("")) {
            getData(listSP);
        } else {
            ArrayList<SanPham> listResult = new ArrayList<>();
            for (int i = 0; i < listSP.size(); i++) {
                if (listSP.get(i).getTenSP().contains(tuKhoa) || listSP.get(i).getMaSP().contains(tuKhoa)) {
                    listResult.add(listSP.get(i));
                }
            }
            getData(listResult);
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String maSP = txt_maSP.getText();
        String tenSP = txt_tenSP.getText();
        String dvt = txt_DVT.getText();
        String tenNSX = txt_tenNSX.getText();
        String giaNhap = txt_giaNhap.getText();
        String giaBan = txt_giaBan.getText();
        String soLuong = txt_soLuong.getText();

        if (maSP.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm không được để trống");
            return;
        } else if (tenSP.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên sản phẩm không được để trống");
            return;
        } else if (dvt.equals("")) {
            JOptionPane.showMessageDialog(this, "Đơn vị tính không được để trống");
            return;
        } else if (tenNSX.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên NSX không được để trống");
            return;
        } else if (giaNhap.equals("")) {
            JOptionPane.showMessageDialog(this, "Giá nhập không được để trống");
            return;
        } else if (giaBan.equals("")) {
            JOptionPane.showMessageDialog(this, "Giá bán không được để trống");
            return;
        } else if (soLuong.equals("")) {
            JOptionPane.showMessageDialog(this, "Số lượng không được để trống");
            return;
        } else {
            SanPham sp = new SanPham();
            sp.setDVT(dvt);
            sp.setGiaBan(Double.parseDouble(giaBan));
            sp.setGiaNhap(Double.parseDouble(giaNhap));
            sp.setMaSP(maSP);
            sp.setSoLuong(Integer.parseInt(soLuong));
            sp.setTenNSX(tenNSX);
            sp.setTenSP(tenSP);
            listSP.add(sp);
            saveFile();
            getData(listSP);
            JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công");
            reset();
        }

    }

    public void reset() {
        txt_DVT.setText("");
        txt_giaBan.setText("");
        txt_giaNhap.setText("");
        txt_maSP.setText("");
        txt_soLuong.setText("");
        txt_tenNSX.setText("");
        txt_tenSP.setText("");
        btn_them.setEnabled(true);
        txt_maSP.setEnabled(true);
        getData(listSP);
    }//GEN-LAST:event_btn_themActionPerformed

