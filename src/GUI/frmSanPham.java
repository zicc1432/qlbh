    private void tbl_sanphamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_sanphamMouseClicked
        // TODO add your handling code here:
        int row = tbl_sanpham.getSelectedRow();
        String maSP = tbl_sanpham.getValueAt(row, 0).toString();
        for (int i = 0; i < listSP.size(); i++) {
            if (maSP.equalsIgnoreCase(listSP.get(i).getMaSP())) {
                sp = listSP.get(i);
                index = i;
                break;

            }

        }
        txt_maSP.setEnabled(false);
        txt_maSP.setText(sp.getMaSP());
        txt_tenSP.setText(sp.getTenSP());
        txt_DVT.setText(sp.getDVT());
        txt_tenNSX.setText(sp.getTenNSX());
        txt_giaNhap.setText(Double.toString(sp.getGiaNhap()));
        txt_giaBan.setText(Double.toString(sp.getGiaBan()));
        txt_soLuong.setText(Integer.toString(sp.getSoLuong()));

        btn_them.setEnabled(false);

    }//GEN-LAST:event_tbl_sanphamMouseClicked


