    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        String tuKhoa = txt_tuKhoa.getText();
        if (tuKhoa.equals("")) {
            getData(listKH);
        } else {
            ArrayList<KhachHang> listResult = new ArrayList<>();
            for (int i = 0; i < listKH.size(); i++) {
                if (listKH.get(i).getTenKH().contains(tuKhoa) || listKH.get(i).getMaKH().contains(tuKhoa)) {
                    listResult.add(listKH.get(i));
                }
            }
            getData(listResult);
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        String maKH = txt_maKH.getText();
        String tenKH = txt_tenKH.getText();
        String email = txt_email.getText();
        String sdt = txt_sdt.getText();
        String diaChi = txt_diaChi.getText();
        String gioiTinh = "";
        if (txt_gioiTinh.getSelectedItem().equals("Nam")) {
            gioiTinh = "Nam";
        } else if (txt_gioiTinh.getSelectedItem().equals("Nữ")) {
            gioiTinh = "Nữ";
        } else {
            gioiTinh = "Khác";
        }

        int trangThai = 0;
        if (txt_trangThai.getSelectedItem().equals("Hoạt động")) {
            trangThai = 1;
        } else {
            trangThai = 0;
        }

        if (!check.kiemTraTen(tenKH)) {
            JOptionPane.showMessageDialog(this, "Tên không được chứa số");
            return;
        }
        if (!check.kiemTraEmail(email)) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng");
            return;
        }
        if (check.kiemTraSDT(sdt)) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không đúng định dạng");
            return;
        }

        if (maKH.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã khách hàng không được để trống");
            return;
        } else if (tenKH.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên khách hàng không được để trống");
            return;
        } else if (sdt.equals("")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống");
            return;
        } else if (diaChi.equals("")) {
            JOptionPane.showMessageDialog(this, "Địa chỉ được để trống");
            return;
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(this, "Email không được để trống");
            return;
        } else {
            KhachHang kh = new KhachHang();
            kh.setMaKH(maKH);
            kh.setTenKH(tenKH);
            kh.setEmail(email);
            kh.setSoDienThoai(sdt);
            kh.setDiaChi(diaChi);
            kh.setGioiTinh(gioiTinh);
            kh.setTrangThai(trangThai);
            listKH.add(kh);
            saveFile();
            getData(listKH);
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công");
            reset();
        }

    }

    public void reset() {
        txt_email.setText("");
        txt_gioiTinh.setSelectedIndex(0);
        txt_maKH.setText("");
        txt_sdt.setText("");
        txt_tenKH.setText("");
        txt_trangThai.setSelectedIndex(0);
        txt_diaChi.setText("");
        btn_them.setEnabled(true);
        txt_maKH.setEnabled(true);
        getData(listKH);

    }//GEN-LAST:event_btn_themActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Integer confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa không?", "Xóa", 2);
        if (confirm == JOptionPane.YES_OPTION) {
            if (listKH.remove(kh)) {
                saveFile();
                getData(listKH);
                JOptionPane.showMessageDialog(this, "Xóa thành công khách hàng" + kh.getTenKH());
            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        }

        reset();
    }//GEN-LAST:event_jButton3ActionPerformed

